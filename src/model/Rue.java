public class Rue {
    public String name;
    public int tmps;

    public Rue(String name, int tmps) {
        this.name = name;
        this.tmps = tmps;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTmps() {
        return tmps;
    }

    public void setTmps(int tmps) {
        this.tmps = tmps;
    }
}
